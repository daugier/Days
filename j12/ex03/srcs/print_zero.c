#include "ft.h"

void	print_zero(int i)
{
	if (i < 16)
		ft_putstr("00000");
	if (i < 100 && i > 15)
		ft_putstr("0000");
	if (i < 256 && i > 99)
		ft_putstr("0000");
	if (i < 10000 && i > 255)
		ft_putstr("000");
	if (i < 100000 && i > 9999)
		ft_putstr("0");
}
